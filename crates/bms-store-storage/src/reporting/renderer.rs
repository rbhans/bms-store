use crate::store::report_store::{ReportError, TimeRangeKind};

// ----------------------------------------------------------------
// Rendered data types — produced by engine, consumed by renderer
// ----------------------------------------------------------------

pub struct SummaryRow {
    pub label: String,
    pub device_id: String,
    pub point_id: String,
    pub min: f64,
    pub max: f64,
    pub avg: f64,
    pub sample_count: usize,
}

pub struct RuntimeRow {
    pub label: String,
    pub device_id: String,
    pub point_id: String,
    pub on_percent: f64,
    pub total_hours: f64,
    pub on_hours: f64,
}

pub struct AlarmEventRow {
    pub timestamp_ms: i64,
    pub device_id: String,
    pub point_id: String,
    pub severity: String,
    pub from_state: String,
    pub to_state: String,
    pub value: f64,
}

pub struct CurrentValueRow {
    pub label: String,
    pub device_id: String,
    pub point_id: String,
    pub value: f64,
    pub status: String,
}

pub struct SeverityCount {
    pub name: String,
    pub count: usize,
}

pub struct TypeCount {
    pub name: String,
    pub count: usize,
}

pub enum RenderedSection {
    HistorySummary {
        title: String,
        rows: Vec<SummaryRow>,
    },
    AlarmSummary {
        title: String,
        by_severity: Vec<SeverityCount>,
        by_type: Vec<TypeCount>,
        total: usize,
    },
    AlarmList {
        title: String,
        events: Vec<AlarmEventRow>,
    },
    CurrentValues {
        title: String,
        rows: Vec<CurrentValueRow>,
    },
    RuntimeSummary {
        title: String,
        rows: Vec<RuntimeRow>,
    },
    EnergyConsumption {
        title: String,
        rows: Vec<EnergyRow>,
    },
    DemandSummary {
        title: String,
        rows: Vec<DemandRow>,
    },
}

#[derive(Debug, Clone)]
pub struct EnergyRow {
    pub label: String,
    pub consumption_kwh: f64,
    pub peak_kw: f64,
    pub avg_kw: f64,
    pub cost: f64,
}

#[derive(Debug, Clone)]
pub struct DemandRow {
    pub label: String,
    pub peak_kw: f64,
    pub peak_time_ms: i64,
    pub load_factor: f64,
}

