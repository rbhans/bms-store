fn render_section_header(html: &mut String, title: &str) {
    html.push_str(&format!(
        "<div style=\"{}\">\n",
        "background:#16213e;border-radius:8px;padding:20px;margin-bottom:16px;border:1px solid #2a2a4a"
    ));
    html.push_str(&format!(
        "<h2 style=\"margin:0 0 12px 0;font-size:16px;color:#e0e0e0;border-bottom:1px solid #2a2a4a;padding-bottom:8px;\">{}</h2>\n",
        escape_html(title)
    ));
}

fn render_empty(html: &mut String, message: &str) {
    html.push_str(&format!(
        "<p style=\"color:#808090;font-style:italic;\">{}</p>\n</div>\n",
        escape_html(message)
    ));
}

fn escape_html(s: &str) -> String {
    s.replace('&', "&amp;")
        .replace('<', "&lt;")
        .replace('>', "&gt;")
        .replace('"', "&quot;")
}

fn format_datetime(ms: i64) -> String {
    // Simple UTC datetime formatting without external crate
    let secs = ms / 1000;
    let days = secs / 86400;
    let time_secs = secs % 86400;
    let hours = time_secs / 3600;
    let mins = (time_secs % 3600) / 60;

    // Compute date from days since epoch (1970-01-01)
    let (year, month, day) = days_to_ymd(days);
    format!("{year:04}-{month:02}-{day:02} {hours:02}:{mins:02} UTC")
}

/// Convert days since Unix epoch to (year, month, day).
fn days_to_ymd(days: i64) -> (i64, i64, i64) {
    // Algorithm from Howard Hinnant's date library
    let z = days + 719468;
    let era = if z >= 0 { z } else { z - 146096 } / 146097;
    let doe = (z - era * 146097) as u64;
    let yoe = (doe - doe / 1460 + doe / 36524 - doe / 146096) / 365;
    let y = yoe as i64 + era * 400;
    let doy = doe - (365 * yoe + yoe / 4 - yoe / 100);
    let mp = (5 * doy + 2) / 153;
    let d = doy - (153 * mp + 2) / 5 + 1;
    let m = if mp < 10 { mp + 3 } else { mp - 9 };
    let y = if m <= 2 { y + 1 } else { y };
    (y, m as i64, d as i64)
}

fn severity_color(severity: &str) -> &'static str {
    match severity.to_lowercase().as_str() {
        "life safety" | "lifesafety" => "#e74c3c",
        "critical" => "#e67e22",
        "warning" => "#f1c40f",
        "info" => "#3498db",
        _ => "#a0a0b0",
    }
}

// ---- Inline CSS constants ----

const TABLE_STYLE: &str = "width:100%;border-collapse:collapse;margin-bottom:4px;";
const TH_STYLE: &str = "padding:8px 12px;text-align:left;font-size:12px;color:#808090;font-weight:600;text-transform:uppercase;letter-spacing:0.5px;border-bottom:1px solid #2a2a4a;";
const TD_STYLE: &str =
    "padding:6px 12px;font-size:13px;color:#d0d0e0;border-bottom:1px solid #1e1e3a;";
const ROW_EVEN: &str = "transparent";
const ROW_ODD: &str = "#1a1a30";

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_escape_html() {
        assert_eq!(escape_html("<b>test</b>"), "&lt;b&gt;test&lt;/b&gt;");
        assert_eq!(escape_html("a & b"), "a &amp; b");
    }

    #[test]
    fn test_format_datetime() {
        // 2024-01-01 00:00 UTC
        let ts = 1704067200000_i64;
        let s = format_datetime(ts);
        assert!(s.contains("2024-01-01"));
        assert!(s.contains("00:00"));
    }

    #[test]
    fn test_render_empty_report() {
        let result = render_report_html(
            "Test Report",
            1704067200000,
            1704153600000,
            &TimeRangeKind::Last24Hours,
            &[],
        )
        .unwrap();
        assert!(result.contains("Test Report"));
        assert!(result.contains("Generated by OpenCrate BMS"));
        assert!(result.contains("<!DOCTYPE html>"));
    }

    #[test]
    fn test_render_history_summary() {
        let sections = vec![RenderedSection::HistorySummary {
            title: "Energy".to_string(),
            rows: vec![SummaryRow {
                label: "AHU-1/Power".to_string(),
                device_id: "ahu-1".to_string(),
                point_id: "power".to_string(),
                min: 10.5,
                max: 95.3,
                avg: 52.1,
                sample_count: 1440,
            }],
        }];
        let result = render_report_html(
            "Energy Report",
            1704067200000,
            1704153600000,
            &TimeRangeKind::Last24Hours,
            &sections,
        )
        .unwrap();
        assert!(result.contains("Energy"));
        assert!(result.contains("AHU-1/Power"));
        assert!(result.contains("10.50"));
        assert!(result.contains("95.30"));
    }
}
