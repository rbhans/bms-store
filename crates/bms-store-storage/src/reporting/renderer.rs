/// Render a complete report to self-contained HTML with inline CSS.
pub fn render_report_html(
    title: &str,
    start_ms: i64,
    end_ms: i64,
    time_range: &TimeRangeKind,
    sections: &[RenderedSection],
) -> Result<String, ReportError> {
    let now = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .unwrap_or_default()
        .as_millis() as i64;

    let mut html = String::with_capacity(8192);

    // Document start
    html.push_str("<!DOCTYPE html>\n<html>\n<head>\n<meta charset=\"utf-8\">\n");
    html.push_str(&format!("<title>{}</title>\n", escape_html(title)));
    html.push_str("</head>\n");
    html.push_str(&format!(
        "<body style=\"{}\">\n",
        "margin:0;padding:0;background:#1a1a2e;color:#e0e0e0;font-family:-apple-system,BlinkMacSystemFont,'Segoe UI',Roboto,sans-serif;font-size:14px;line-height:1.5"
    ));

    // Container
    html.push_str("<div style=\"max-width:800px;margin:0 auto;padding:24px;\">\n");

    // Header
    html.push_str(&format!(
        "<div style=\"{}\">\n",
        "background:#16213e;border-radius:8px;padding:24px;margin-bottom:24px;border:1px solid #2a2a4a"
    ));
    html.push_str(&format!(
        "<h1 style=\"margin:0 0 8px 0;font-size:22px;color:#f0f0f0;\">{}</h1>\n",
        escape_html(title)
    ));
    html.push_str(&format!(
        "<p style=\"margin:0;color:#a0a0b0;font-size:13px;\">Period: {} &mdash; {} to {}</p>\n",
        escape_html(time_range.label()),
        format_datetime(start_ms),
        format_datetime(end_ms),
    ));
    html.push_str(&format!(
        "<p style=\"margin:4px 0 0 0;color:#808090;font-size:12px;\">Generated: {}</p>\n",
        format_datetime(now),
    ));
    html.push_str("</div>\n");

    // Sections
    for section in sections {
        render_section(&mut html, section);
    }

    // Footer
    html.push_str(&format!(
        "<div style=\"{}\">\n",
        "margin-top:32px;padding-top:16px;border-top:1px solid #2a2a4a;text-align:center;color:#606070;font-size:12px"
    ));
    html.push_str("Generated by OpenCrate BMS\n</div>\n");

    // Close
    html.push_str("</div>\n</body>\n</html>");

    Ok(html)
}

fn render_section(html: &mut String, section: &RenderedSection) {
    match section {
        RenderedSection::HistorySummary { title, rows } => {
            render_section_header(html, title);
            if rows.is_empty() {
                render_empty(
                    html,
                    "No history data available for the selected points and time range.",
                );
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Point", "Min", "Max", "Avg", "Samples"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, row) in rows.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.label)
                ));
                html.push_str(&format!(
                    "<td style=\"{}{}\">",
                    TD_STYLE, "text-align:right;"
                ));
                html.push_str(&format!("{:.2}</td>", row.min));
                html.push_str(&format!(
                    "<td style=\"{}{}\">",
                    TD_STYLE, "text-align:right;"
                ));
                html.push_str(&format!("{:.2}</td>", row.max));
                html.push_str(&format!(
                    "<td style=\"{}{}\">",
                    TD_STYLE, "text-align:right;"
                ));
                html.push_str(&format!("{:.2}</td>", row.avg));
                html.push_str(&format!(
                    "<td style=\"{}{}\">",
                    TD_STYLE, "text-align:right;"
                ));
                html.push_str(&format!("{}</td>", row.sample_count));
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }

        RenderedSection::AlarmSummary {
            title,
            by_severity,
            by_type,
            total,
        } => {
            render_section_header(html, title);
            html.push_str(&format!(
                "<p style=\"color:#a0a0b0;margin:0 0 12px 0;\">Total alarm events: <strong style=\"color:#f0f0f0;\">{total}</strong></p>\n"
            ));

            if !by_severity.is_empty() {
                html.push_str("<p style=\"color:#c0c0d0;margin:8px 0 4px 0;font-weight:600;\">By Severity</p>\n");
                html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
                html.push_str(&format!("<tr><th style=\"{}\">Severity</th><th style=\"{}text-align:right;\">Count</th></tr>\n", TH_STYLE, TH_STYLE));
                for (i, sc) in by_severity.iter().enumerate() {
                    let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                    let color = severity_color(&sc.name);
                    html.push_str(&format!(
                        "<tr style=\"background:{bg}\"><td style=\"{}\"><span style=\"color:{color};\">{}</span></td><td style=\"{}text-align:right;\">{}</td></tr>\n",
                        TD_STYLE, escape_html(&sc.name), TD_STYLE, sc.count,
                    ));
                }
                html.push_str("</table>\n");
            }

            if !by_type.is_empty() {
                html.push_str("<p style=\"color:#c0c0d0;margin:16px 0 4px 0;font-weight:600;\">By Transition</p>\n");
                html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
                html.push_str(&format!("<tr><th style=\"{}\">State</th><th style=\"{}text-align:right;\">Count</th></tr>\n", TH_STYLE, TH_STYLE));
                for (i, tc) in by_type.iter().enumerate() {
                    let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                    html.push_str(&format!(
                        "<tr style=\"background:{bg}\"><td style=\"{}\">{}</td><td style=\"{}text-align:right;\">{}</td></tr>\n",
                        TD_STYLE, escape_html(&tc.name), TD_STYLE, tc.count,
                    ));
                }
                html.push_str("</table>\n");
            }
            html.push_str("</div>\n");
        }

        RenderedSection::AlarmList { title, events } => {
            render_section_header(html, title);
            if events.is_empty() {
                render_empty(html, "No alarm events in the selected time range.");
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Time", "Device", "Point", "Severity", "Transition", "Value"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, ev) in events.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                let color = severity_color(&ev.severity);
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}white-space:nowrap;\">{}</td>",
                    TD_STYLE,
                    format_datetime(ev.timestamp_ms)
                ));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&ev.device_id)
                ));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&ev.point_id)
                ));
                html.push_str(&format!(
                    "<td style=\"{}\"><span style=\"color:{color};\">{}</span></td>",
                    TD_STYLE,
                    escape_html(&ev.severity)
                ));
                html.push_str(&format!(
                    "<td style=\"{}\">{} → {}</td>",
                    TD_STYLE,
                    escape_html(&ev.from_state),
                    escape_html(&ev.to_state)
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.2}</td>",
                    TD_STYLE, ev.value
                ));
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }

        RenderedSection::CurrentValues { title, rows } => {
            render_section_header(html, title);
            if rows.is_empty() {
                render_empty(html, "No current values available.");
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Point", "Value", "Status"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, row) in rows.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.label)
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.2}</td>",
                    TD_STYLE, row.value
                ));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.status)
                ));
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }

        RenderedSection::RuntimeSummary { title, rows } => {
            render_section_header(html, title);
            if rows.is_empty() {
                render_empty(html, "No runtime data available.");
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Equipment", "On %", "On Hours", "Total Hours"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, row) in rows.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                let bar_color = if row.on_percent > 90.0 {
                    "#c0392b"
                } else if row.on_percent > 50.0 {
                    "#e67e22"
                } else {
                    "#27ae60"
                };
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.label)
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\"><span style=\"color:{bar_color};font-weight:600;\">{:.1}%</span></td>",
                    TD_STYLE, row.on_percent,
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.1}</td>",
                    TD_STYLE, row.on_hours
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.1}</td>",
                    TD_STYLE, row.total_hours
                ));
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }

        RenderedSection::EnergyConsumption { title, rows } => {
            render_section_header(html, title);
            if rows.is_empty() {
                render_empty(html, "No energy data available.");
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Meter", "kWh", "Peak kW", "Avg kW", "Cost"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, row) in rows.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.label)
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.1}</td>",
                    TD_STYLE, row.consumption_kwh
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.1}</td>",
                    TD_STYLE, row.peak_kw
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.2}</td>",
                    TD_STYLE, row.avg_kw
                ));
                if row.cost > 0.0 {
                    html.push_str(&format!(
                        "<td style=\"{}text-align:right;\">{:.2}</td>",
                        TD_STYLE, row.cost
                    ));
                } else {
                    html.push_str(&format!(
                        "<td style=\"{}text-align:right;\">—</td>",
                        TD_STYLE
                    ));
                }
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }

        RenderedSection::DemandSummary { title, rows } => {
            render_section_header(html, title);
            if rows.is_empty() {
                render_empty(html, "No demand data available.");
                return;
            }
            html.push_str(&format!("<table style=\"{}\">\n", TABLE_STYLE));
            html.push_str("<tr>");
            for h in &["Meter", "Peak kW", "Load Factor"] {
                html.push_str(&format!("<th style=\"{}\">{}</th>", TH_STYLE, h));
            }
            html.push_str("</tr>\n");
            for (i, row) in rows.iter().enumerate() {
                let bg = if i % 2 == 0 { ROW_EVEN } else { ROW_ODD };
                let lf_color = if row.load_factor > 0.7 {
                    "#27ae60"
                } else if row.load_factor > 0.4 {
                    "#e67e22"
                } else {
                    "#c0392b"
                };
                html.push_str(&format!("<tr style=\"background:{bg}\">"));
                html.push_str(&format!(
                    "<td style=\"{}\">{}</td>",
                    TD_STYLE,
                    escape_html(&row.label)
                ));
                html.push_str(&format!(
                    "<td style=\"{}text-align:right;\">{:.1}</td>",
                    TD_STYLE, row.peak_kw
                ));
                html.push_str(&format!("<td style=\"{}text-align:right;\"><span style=\"color:{lf_color};font-weight:600;\">{:.1}%</span></td>", TD_STYLE, row.load_factor * 100.0));
                html.push_str("</tr>\n");
            }
            html.push_str("</table>\n</div>\n");
        }
    }
}

